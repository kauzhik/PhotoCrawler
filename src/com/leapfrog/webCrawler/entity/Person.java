package com.leapfrog.webCrawler.entity;

/**
 *
 * @author kaushik
 */
public class Person {
    private int id;
    private String name;
    private int serialNo;

    public Person() {
    }
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSerialNo() {
        return serialNo;
    }

    public void setSerialNo(int serialNo) {
        this.serialNo = serialNo;
    }
    
}
